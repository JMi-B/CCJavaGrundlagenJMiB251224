package thema1grundlagen;

public class Primitive3 {
	//float 
public static void main(String[] args) {
	
	float f = 3.6f ;
	float g = 7.2f ;
	
	System.out.println( " f = " + f +  " g =" + g );
	
	System.out.println(f+g);
	System.out.println(f-g);
	System.out.println(f*g);
	System.out.println(f/g);
	System.out.println(f%g);

	}
}
